function extractKeywords(lawName, articleNo, content) {
  const tags = [lawName];
  const patterns = [
    /申報/g, /繳納/g, /免稅/g, /扣除/g, /罰則/g, /罰鍰/g,
    /停業/g, /廢止/g, /罰款/g, /補稅/g, /退稅/g, /課稅/g,
    /會計師/g, /記帳士/g, /稽徵/g, /稅率/g, /稅額/g,
    /保費/g, /投保/g, /給付/g, /退休/g, /資遣/g,
    /勞工/g, /雇主/g, /勞動契約/g,
  ];
  for (const p of patterns) {
    const key = p.source;
    if (p.test(content)) tags.push(key);
  }
  return [...new Set(tags)].slice(0, 6);
}

function buildTitle(articleNo, content) {
  // Take first clause up to first 、or 。or newline, max 30 chars
  const first = content.split(/[。\n]/)[0].replace(/\s+/g, ' ').trim();
  if (first.length <= 30) return `${articleNo} ${first}`;
  return `${articleNo} ${first.slice(0, 28)}…`;
}

// ── Main ──────────────────────────────────────────────────────────────────────
const zipPath = path.join(__dirname, '..', 'tmp_ChLaw.zip');
if (!fs.existsSync(zipPath)) {
  console.error('tmp_ChLaw.zip not found. Run the download first.');
  process.exit(1);
}

console.log('Extracting ZIP...');
const zip = new AdmZip(zipPath);
const entry = zip.getEntry('ChLaw.json');
if (!entry) {
  console.error('ChLaw.json not found in ZIP');
  process.exit(1);
}
let rawJson = entry.getData().toString('utf8');
if (rawJson.charCodeAt(0) === 0xFEFF) rawJson = rawJson.slice(1); // strip BOM
const parsed = JSON.parse(rawJson);
const json = parsed.Laws ?? parsed; // schema: { UpdateDate, Laws: [...] }
console.log(`Loaded ${json.length} laws from ChLaw.json`);

// Build a map for quick lookup
const lawMap = {};
for (const law of json) {
  if (TARGET_LAWS.includes(law.LawName)) {
    lawMap[law.LawName] = law;
  }
}
console.log('Found target laws:', Object.keys(lawMap).join(', '));

// Generate cards
let nextId = 2000; // start after existing max (1105)
const cards = [];

for (const lawName of TARGET_LAWS) {
  const law = lawMap[lawName];
  if (!law) {
    console.warn(`  ⚠  Not found in ChLaw.json: ${lawName}`);
    continue;
  }

  const category = LAW_CATEGORY[lawName] || lawName;
  let articleCount = 0;

  for (const article of law.LawArticles) {
    {
      if (article.ArticleType !== 'A') continue; // skip chapter headings
      // ArticleNo is already like "第 1 條" – normalise spacing
      const no = article.ArticleNo ? article.ArticleNo.replace(/\s+/g, '') : '';
      const content = (article.ArticleContent || '').replace(/\r\n/g, '\n').trim();
      if (!content || content.length < 5) continue;

      cards.push({
        cardId: nextId++,
        lawName,
        article: no,
        summary: buildTitle(no, content),
        content,
        category,
        searchKeywords: extractKeywords(lawName, no, content).join(' '),
        isActive: 1,
      });
      articleCount++;
    }}
  console.log(`  ${lawName}: ${articleCount} articles → ${articleCount} cards`);
}

console.log(`\nTotal new cards: ${cards.length} (IDs ${2000}–${nextId - 1})`);

// ── Emit TypeScript patch ──────────────────────────────────────────────────────
function escapeStr(s) {
  return s.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\${/g, '\\${');
}

