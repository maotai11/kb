lines.push("    'UPDATE law_cards SET is_active = 0 WHERE law_name = ? AND card_id < 2000'");
lines.push('  );');
lines.push('  const deactivateAll = db.transaction(() => {');
lines.push('    for (const law of coveredLaws) deactivate.run(law);');
lines.push('  });');
lines.push('  deactivateAll();');
lines.push('}');

